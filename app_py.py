import streamlit as st
import pandas as pd
from sklearn.linear_model import LinearRegression

# Model training inside the app for simplicity
def train_model():
    data = {
        'Rooms': [3, 4, 5, 7, 6],
        'Area_sqft': [1200, 1500, 2000, 1000, 2500],
        'Area_aana': [4, 4, 6, 3, 7],
        'Price_NPR': [81810, 83440, 126950, 176337, 154404]
    }
    df = pd.DataFrame(data)
    X = df[['Rooms', 'Area_sqft', 'Area_aana']]
    y = df['Price_NPR']
    model = LinearRegression()
    model.fit(X, y)
    return model

# Streamlit UI
st.title("üè† Nepali House Price Predictor")

st.sidebar.header("Enter House Details:")
rooms = st.sidebar.number_input("Number of Rooms", min_value=1, value=4)
area_sqft = st.sidebar.number_input("Area in Square Feet", min_value=100, value=1500)
area_aana = st.sidebar.number_input("Area in Aana", min_value=1, value=5)

# Predict button
if st.sidebar.button("Predict Price"):
    model = train_model()
    input_df = pd.DataFrame({
        'Rooms': [rooms],
        'Area_sqft': [area_sqft],
        'Area_aana': [area_aana]
    })
    predicted_price = model.predict(input_df)[0]
    st.success(f"Estimated House Price: NPR {predicted_price:,.0f}")